package sourcescode.packagetypesourcecode;

/**
 * Float package type demo
 * */
public class FloatPackageTypeDemo {
   public static void main(String[] args){
       System.out.println(Float.POSITIVE_INFINITY); //Infinity more than more
       System.out.println(Float.NEGATIVE_INFINITY); //-Infinity
       System.out.println(Float.NaN);               //NaN
       System.out.println(Float.MIN_VALUE);         //1.4E-45
       System.out.println(Float.MIN_NORMAL);        //1.17549435E-38
       System.out.println(Float.MIN_EXPONENT);      //-126
       System.out.println(Float.SIZE);              //32bit
       System.out.println(Float.BYTES);             //4bytes
   }

}
